numbers = [3, 1, 4, 1, 5, 9, 2, 6]

# Length of the list
print("Length of the list of numbers:", len(numbers))

# Sorted list
print("Sorted list of numbers:", sorted(numbers))

# Maximum value
print("Maximum value in the list of numbers:", max(numbers))

# Minimum value
print("Minimum value in the list of numbers:", min(numbers))